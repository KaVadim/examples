// прога, кот выводит массив из 8 элементов, заполненный нулями и еденицами в случайном порядке
// 1,1,0,0,1,1,0,1,0
Console.Clear();
int [] arr = new int [8]; // объявляется массив и  выделяется на него память
for (int i=0; i < arr.Length; i++)
{
    arr[i] = new Random().Next(0,2); // рандом, последний элемент "2" не включается
    // Console.Write(" " + arr[i]);
}
Console.Write('[' + string.Join(", ", arr) + ']'); // вариант вывода Симпатичнее